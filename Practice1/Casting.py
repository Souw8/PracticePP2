#Today we going to disguss about type casting
#int() - constructs an integer number from an integer literal, a float literal (by removing all decimals), or a string literal (providing the string represents a whole number)
#float() - constructs a float number from an integer literal, a float literal or a string literal (providing the string represents a float or an integer)
#str() - constructs a string from a wide variety of data types, including strings, integer literals and float literals
x = int(22)   # x will be 22
y = int(2.99) # y will be 2.99
z = int("7") # z will be 7
x = float(5)     # x will be 5.0
y = float(7.8)   # y will be 7.8
z = float("8")   # z will be 8.0
w = float("4.5") # w will be 4.5
x = str("s2") # x will be 's2'
y = str(33)    # y will be '33'
z = str(7.0)  # z will be '7.0'
